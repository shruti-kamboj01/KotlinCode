// to declare a variable starting with keyword val or var
//1. val keyword is used to declare variables that are assigned a value only once.
//these are immutable, read-only local variables
//2. var keyword us used to declare variables that can be reassigned.

fun main() {
    var x: Int = 5
    x += 1
    println(x)

    val y: Int
    y = 4

    println(y)


}

